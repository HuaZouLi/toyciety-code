type I18nContextValue = {
  locale: AppLocale;
  language: AppLanguagePreference;
  t: Translate;
  tx: TranslateText;
};

const fallbackLocale: AppLocale = "en";

function getNavigatorLanguages(): readonly string[] {
  if (typeof navigator === "undefined") {
    return [];
  }
  if (Array.isArray(navigator.languages) && navigator.languages.length > 0) {
    return navigator.languages;
  }
  return navigator.language ? [navigator.language] : [];
}

function interpolate(template: string, values?: TranslationValues): string {
  if (!values) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    const value = values[key];
    return value == null ? match : String(value);
  });
}

export function normalizeLanguagePreference(value: unknown): AppLanguagePreference {
  return value === "en" || value === "zh-CN" || value === "system"
    ? value
    : "system";
}

export function resolveLocale(
  language: AppLanguagePreference,
  systemLanguages: readonly string[] = getNavigatorLanguages(),
): AppLocale {
  if (language !== "system") {
    return language;
  }
  const firstMatch = systemLanguages.find((entry) => entry.trim().length > 0);
  return firstMatch?.toLowerCase().startsWith("zh") ? "zh-CN" : fallbackLocale;
}

export function translate(
  locale: AppLocale,
  key: TranslationKey,
  values?: TranslationValues,
): string {
  const template = translations[locale][key] ?? translations[fallbackLocale][key] ?? key;
  return interpolate(template, values);
}

export function translateText(
  locale: AppLocale,
  source: string,
  values?: TranslationValues,
): string {
  const template =
    locale === "zh-CN"
      ? (zhCNTextTranslations[source] ?? zhCNAdditionalTextTranslations[source])
      : undefined;
  return interpolate(template ?? source, values);
}

const defaultI18nContext: I18nContextValue = {
  locale: fallbackLocale,
  language: "system",
  t: (key, values) => translate(fallbackLocale, key, values),
  tx: (source, values) => translateText(fallbackLocale, source, values),
};

const I18nContext = createContext<I18nContextValue>(defaultI18nContext);

export function I18nProvider({
  children,
  language,
}: {
  children: ReactNode;
  language: AppLanguagePreference;
}) {
  const normalizedLanguage = normalizeLanguagePreference(language);
  const locale = resolveLocale(normalizedLanguage);

  useEffect(() => {
    if (typeof document === "undefined") {
      return;
    }
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      language: normalizedLanguage,
      t: (key, values) => translate(locale, key, values),
      tx: (source, values) => translateText(locale, source, values),
    }),
    [locale, normalizedLanguage],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext);
}
